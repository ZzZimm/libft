#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void*, size_t))
{
	t_list *tmp;

	while (*alst)
	{
		tmp = (*alst)->next;
		del((*alst)->content, (*alst)->content_size);
		(*alst)->content_size = 0;
		free(*alst);
		*alst = tmp;
	}
	*alst = NULL;
}
